## Equality operator 

# This line sets the value of car to audi
# using a single equal sign, as normal.
car = 'audi'

# This line uses an equality operator, a double equal sign, 
# to check whether the value matches our desired value, 
# returning true or false.

car == 'bmw'

# A single equal sign is really a statement. 
# A double equal sign asking a question, is the value = to x? 